package encapsulamiento;

public class Operaciones {
   
    //Metodo sumar
    public int sumar(int n1, int n2){
        int suma = n1 + n2;
        return suma;
    }
    
    // Metodo restar
    public int restar(int n1, int n2){
        int resta = n1 - n2;
        
        return resta;
    }
}
