package operadoresrelacionales;

public class OperadoresRelacionales {

    public static void main(String[] args) {
        int a = 10, b = 5;
        
        // Operadores relacionales
        System.out.println(" a = " + a);
        System.out.println(" b = " + b);
        
        System.out.println("a == b " + (a == b));
        System.out.println("a < b " + (a < b));
        System.out.println("a <= b " + (a <= b));
        System.out.println("a > b " + (a > b));
        System.out.println("a >= b " + (a >= b));
        System.out.println("a != b " + (a !=b));
    }  
}
